<?php

namespace FormBundle\Entity;

/**
 * Formations
 */
class Formations
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $photoOrganisme;

    /**
     * @var string
     */
    private $titre;

    /**
     * @var integer
     */
    private $nbPlace;

    /**
     * @var string
     */
    private $lieu;

    /**
     * @var string
     */
    private $resume;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set photoOrganisme
     *
     * @param string $photoOrganisme
     *
     * @return Formations
     */
    public function setPhotoOrganisme($photoOrganisme)
    {
        $this->photoOrganisme = $photoOrganisme;

        return $this;
    }

    /**
     * Get photoOrganisme
     *
     * @return string
     */
    public function getPhotoOrganisme()
    {
        return $this->photoOrganisme;
    }

    /**
     * Set titre
     *
     * @param string $titre
     *
     * @return Formations
     */
    public function setTitre($titre)
    {
        $this->titre = $titre;

        return $this;
    }

    /**
     * Get titre
     *
     * @return string
     */
    public function getTitre()
    {
        return $this->titre;
    }

    /**
     * Set nbPlace
     *
     * @param integer $nbPlace
     *
     * @return Formations
     */
    public function setNbPlace($nbPlace)
    {
        $this->nbPlace = $nbPlace;

        return $this;
    }

    /**
     * Get nbPlace
     *
     * @return integer
     */
    public function getNbPlace()
    {
        return $this->nbPlace;
    }

    /**
     * Set lieu
     *
     * @param string $lieu
     *
     * @return Formations
     */
    public function setLieu($lieu)
    {
        $this->lieu = $lieu;

        return $this;
    }

    /**
     * Get lieu
     *
     * @return string
     */
    public function getLieu()
    {
        return $this->lieu;
    }

    /**
     * Set resume
     *
     * @param string $resume
     *
     * @return Formations
     */
    public function setResume($resume)
    {
        $this->resume = $resume;

        return $this;
    }

    /**
     * Get resume
     *
     * @return string
     */
    public function getResume()
    {
        return $this->resume;
    }
}

